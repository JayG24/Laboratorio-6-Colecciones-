# Se asigna la cadena 'spam' a la variable 's'.
s = 'spam'

# Se convierte la cadena 's' en una lista de caracteres utilizando la función list().
# Esto crea una lista que contiene cada carácter de la cadena como un elemento separado.
t = list(s)

# Imprime la lista 't', que ahora es ['s', 'p', 'a', 'm'].
print(t)

# Se crea una lista vacía llamada 'l' utilizando el constructor list().
l = list()

# Se añade la cadena 's' a la lista 'l' usando el método append().
l.append(s)

# Imprime la lista 'l', que ahora contiene un único elemento: ['spam'].
print(l)