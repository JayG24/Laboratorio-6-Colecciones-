# Se crea una lista llamada 'nums' que contiene varios números enteros.
nums = [3, 41, 12, 9, 74, 15]

# Imprime la longitud de la lista 'nums', es decir, el número de elementos que contiene.
print(len(nums))

# Imprime el valor máximo en la lista 'nums' utilizando la función max().
print(max(nums))

# Imprime el valor mínimo en la lista 'nums' utilizando la función min().
print(min(nums))

# Imprime la suma de todos los elementos en la lista 'nums' utilizando la función sum().
print(sum(nums))

# Imprime el promedio de los elementos en la lista 'nums', calculando la suma dividida por la longitud.
print(sum(nums) / len(nums))
