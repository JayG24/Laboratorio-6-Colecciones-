# Se crea una lista llamada 'numeros' que contiene dos enteros: 17 y 123.
numeros = [17, 123]

# Imprime la lista original 'numeros'.
print(numeros)

# Modifica el segundo elemento de la lista (índice 1) sumándole 1. Ahora será 124.
numeros[1] = numeros[1] + 1

# Imprime la lista después de la modificación, mostrando [17, 124].
print(numeros)

# Cambia el segundo elemento de la lista (índice 1) directamente a 5.
numeros[1] = 5

# Imprime la lista final, que ahora es [17, 5].
print(numeros)