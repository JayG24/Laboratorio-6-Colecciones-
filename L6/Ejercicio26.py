x = ("Kevin", "Brayan", "Jan")  # Se crea una tupla llamada 'x' con tres cadenas
print(x)  # Se imprime la tupla: ('Kevin', 'Brayan', 'Jan')

print(x[2])  # Se imprime el tercer elemento de la tupla (índice 2), que es 'Jan'

y = (1, 9, 2)  # Se crea una tupla llamada 'y' con tres números
print(y)  # Se imprime la tupla: (1, 9, 2)

print(max(y))  # Se imprime el valor máximo de la tupla 'y', que es 9

# Se itera sobre cada elemento en la tupla 'y'
for i in y:
    print(i)  # Se imprime cada elemento de la tupla (1, 9, 2)
